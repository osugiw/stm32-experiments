void get_time(void) 
{ 
 RTC_DateTypeDef gDate; 
 RTC_TimeTypeDef gTime; 
/* Get the RTC current Time */ 
 HAL_RTC_GetTime(&hrtc, &gTime, RTC_FORMAT_BIN); 
/* Get the RTC current Date */ 
 HAL_RTC_GetDate(&hrtc, &gDate, RTC_FORMAT_BIN); 
/* Display time Format: hh:mm:ss */ 
 sprintf((char*)time,"%02d:%02d:%02d",gTime.Hours, gTime.Minutes, gTime.Seconds); 
/* Display date Format: dd-mm-yy */ 
 sprintf((char*)date,"%02d-%02d-%2d",gDate.Date, gDate.Month, 2000 + gDate.Year); 
}

uint8_t convertNumber(uint8_t num)
{
	uint8_t converted;
	switch (num)
	{
		case (0x00):
			converted = NOL;
			break;
		case (0x01):
			converted = SATU;
			break;
		case (0x02):
			converted = DUA;
			break;
		case (0x03):
			converted = TIGA;
			break;
		case (0x04):
			converted = EMPAT;
			break;
		case (0x05):
			converted = LIMA;
			break;
		case (0x06):
			converted = ENAM;
			break;
		case (0x07):
			converted = TUJUH;
			break;
		case (0x08):
			converted = DELAPAN;
			break;
		case (0x09):
			converted = SEMBILAN;
			break;
	}
	return converted;
}

void delay_us (uint16_t us)
{
	__HAL_TIM_SET_COUNTER(&htim1,0);  // set the counter value a 0
	while (__HAL_TIM_GET_COUNTER(&htim1) < us);  // wait for the counter to reach the us input in the parameter
}

void writeByte(uint8_t byte)
{
	for (int i=0; i<8; i++)
	{
		clockLow;
		writePin(GPIOA, GPIO_PIN_4, byte&0x01);	// Write from LSB
		byte = byte >> 1;
		clockHigh;
	}
	delay_us(10);
}

void writeCommand(uint8_t displayCommand)
{
	Strobe0;	// Pull Strobe Low before sending data
	writeByte(displayCommand);
	Strobe1; // Pull Strobe High after sending data
}

void writeData(uint8_t displayData)
{
	Strobe0;	// Pull Strobe Low before sending data
	writeByte(displayData);
	delay_us(10);
}


void turnOn()
{
	writeCommand(incrementAddress);	// Command 2 - Set Write data
	writeCommand(clearDisplay); // Command 3 - Clear RAM Display
	writeCommand(fourDigits); // Command 1
	writeCommand(displayOff); // Command 4 - Display off
	writeCommand(fourDigits); // Command 1
	writeCommand(brightnessLevel8);	// Command 4 - Display on
}

void turnOff()
{
	writeCommand(incrementAddress);	// Command 2 - Set Write data
	writeCommand(clearDisplay); // Command 3 - Clear RAM Display
	writeCommand(fourDigits); // Command 1
	writeCommand(displayOff); // Command 4 - Display off
	writeCommand(fourDigits); // Command 1
	writeCommand(displayOff);	// Command 4 - Display off
}

void displayNilai(uint8_t ribuan, uint8_t ratusan, uint8_t puluhan, uint8_t satuan)
{
	writeCommand(incrementAddress);		// Command 2 - Set Write data
	Strobe1;
	writeData(digit1Seg1);					// Command 3 - Set Address of 7 Segment
	writeData(ribuan);
	writeData(digit2Seg3);					// Command 3 - Set Address of 7 Segment
	writeData(ratusan);
	writeData(digit3Seg5);					// Command 3 - Set Address of 7 Segment
	writeData(puluhan);
	writeData(digit4Seg7);						// Command 3 - Set Address of 7 Segment
	writeData(satuan);
	Strobe1;
	writeCommand(fourDigits); 				// Command 1
	writeCommand(brightnessLevel6);		// Command 4 - Display on
	delay_us(40000);
}

