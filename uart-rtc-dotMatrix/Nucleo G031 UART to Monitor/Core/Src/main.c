void clearBuf()
{
	Monitor_MaxCharRx = strlen(Rx_monitorSerial);
	Device_MaxCharTx = strlen(Tx_deviceSerial);
	
	if(Monitor_MaxCharRx >= 10)
	{
		for(int i=0; i<Monitor_MaxCharRx; i++) {Rx_monitorSerial[i] = '\0';}
	}
	else if (clearState == 1)
	{
		for(int i=0; i<Device_MaxCharTx; i++) {Tx_deviceSerial[i] = '\0';}
		clearState = 0;
	}
}

char* filterData(char a[])
{
	char * bufData;
	char * separator1 = " ";
	bufData = strtok(a, separator1);
	return bufData;
}

void sendCommand()
{
	// Clear data buffer
	clearBuf();

	// Copy Monitor input to buffer before it sends
	strncpy(Tx_deviceSerial, Rx_monitorSerial, 10);
	
	// Enter SPACE ' ' to send data
	checkSpace = strchr(Tx_deviceSerial, ' '); 
	if(checkSpace != NULL)
	{
		cmd = filterData(Tx_deviceSerial);
		for (int i=0; i<strlen(cmd); i++)
		{
			if(strcmp(cmd, command[i]) == 0)
			{
				sendToNucleoG070RB(command[i]);
			}
		}
		/*
		// To reset the serial input monitor
		for(int j=10; j>strlen(cmd); j--)
		{
			if(Rx_monitorSerial[j] == '\0')
			{
				Rx_monitorSerial[j] = ' ';
			}
		}
		*/
		//HAL_Delay(200);
		clearState = 1;
	}
	HAL_Delay(100);
	
	// Copy Device serial data to buffer before send to monitor
	strncpy(Tx_monitorSerial, Rx_deviceSerial, 10);
}

