void i2cCom()
{
	while (buttonState != GPIO_PIN_RESET) {}		// Wait for push button press
	HAL_Delay(50);	// Delay for signal rebound when PB Release
	while (buttonState != GPIO_PIN_SET) {}			// Wait for push button release
		
	// Master data transmission process from master to slave
	do
	{
		if(HAL_I2C_Master_Transmit_IT(&hi2c1, G031_ADDR, (uint8_t*)aTxBuffer, TXBUFFERSIZE) != HAL_OK)
		{
			// Error_Handler() function is called when error occurs
			Error_Handler();	
		}
		
		// Waiting the end of the data transfer process before starting new transfer
		while (HAL_I2C_GetState(&hi2c1) != HAL_I2C_STATE_READY)	{} 
	}
	
	// When Acknowledge failure occurs (Slave don't acknowledge it's address) Master restarts communication
	while(HAL_I2C_GetError(&hi2c1) == HAL_I2C_ERROR_AF);
	
	while (buttonState != GPIO_PIN_RESET) {}		// Wait for push button press
	HAL_Delay(50);	// Delay for signal rebound when PB Release
	while (buttonState != GPIO_PIN_SET) {}			// Wait for push button release
	
	// Master data receive process from slave to master
	do
	{
		if(HAL_I2C_Master_Receive_IT(&hi2c1, G031_ADDR, (uint8_t*)aRxBuffer, RXBUFFERSIZE) != HAL_OK)
		{
			// Error_Handler() function is called when error occurs
			Error_Handler();
		}
		
		// Waiting the end of the data transfer process before starting new transfer
		while (HAL_I2C_GetState(&hi2c1) != HAL_I2C_STATE_READY)	{} 
	}
	
	// When Acknowledge failure occurs (Slave don't acknowledge it's address) Master restarts communication
	while(HAL_I2C_GetError(&hi2c1) == HAL_I2C_ERROR_AF);
	
	while (HAL_I2C_GetState(&hi2c1) != HAL_I2C_STATE_READY){} 
  
  // Compare the sent and received buffers
  if(Buffercmp((uint8_t*)aTxBuffer,(uint8_t*)aRxBuffer,RXBUFFERSIZE))
  {
    // Processing Error
    Error_Handler();      
  }
}

